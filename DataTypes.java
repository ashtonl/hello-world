/*
Basic Data Types:
byte, short, int, long;
float, double;
boolean;
char.

*/

public class DataTypes {
    public static void main(String[] args) {
        char c = 'a';
        System.out.println(c);
        System.out.print('\n');
        System.out.println("\n\"Hello, World!\"");

        long y = 2147483648L;
        System.out.println(y);
    }

}

